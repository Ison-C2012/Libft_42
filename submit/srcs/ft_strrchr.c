#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	s_len;

	if (s == NULL)
		return (NULL);
	s_len = ft_strlen(s);
	if ((char)c == '\0')
		return ((char *)s + s_len);
	while (s_len--)
	{
		if (s[s_len] == (char)c)
			return ((char *)s + s_len);
	}
	return (NULL);
}
