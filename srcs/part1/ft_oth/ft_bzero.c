#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;

	if (s == NULL)
		return ;
	i = 0;
	while (s[i] && i < n)
	{
		s[i] = 0;
		i++;
	}
	return ;
}
